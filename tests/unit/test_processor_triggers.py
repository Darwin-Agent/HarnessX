async def _write_file(proc, path: str, call_id: str):
    """Simulate one Write call+result through the processor; return pipe_all result."""
    ce = ToolCallEvent(
        run_id="r1",
        step_id=0,
        tool_name="Write",
        tool_call_id=call_id,
        tool_input={"file_path": path},
    )
    await pipe(ce, [proc])
    return await pipe_all(
        ToolResultEvent(run_id="r1", step_id=0, tool_name="Write", tool_call_id=call_id, result="ok"),
        [proc],
    )


def _agree_event(run_id="r1", step_id=0) -> ModelResponseEvent:
    """Model response that matches the agreement regex."""
    return ModelResponseEvent(
        run_id=run_id,
        step_id=step_id,
        content="Absolutely! You're completely right about that.",
    )


class _MutatingProcessor(MultiHookProcessor):
    """Processor that silently mutates an event — ProcessorChain should auto-detect."""

    _singleton_group = "test_mutating"

    async def on_before_model(self, event: BeforeModelEvent):
        from harnessx.core.events import Message

        yield BeforeModelEvent(
            run_id=event.run_id,
            step_id=event.step_id,
            messages=event.messages + (Message(role="user", content="auto"),),
        )


class _PassthroughProcessor(MultiHookProcessor):
    """Processor that yields the event unchanged — no trigger should fire."""

    _singleton_group = "test_passthrough"

    async def on_before_model(self, event: BeforeModelEvent):
        yield event


