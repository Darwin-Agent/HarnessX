    @pytest.mark.asyncio
    async def test_parse_retry_per_run_id_isolation(self):
        """Singleton: a valid response from run B must not reset run A's counter."""
        proc = ParseRetryProcessor(max_consecutive_errors=2)
        bad_tc = ToolCall(id="bad", name="", input={})
        good_tc = ToolCall(id="ok", name="Bash", input={"command": "ls"})

        # Run A gets a malformed response — counter reaches 1
        await pipe(ModelResponseEvent(run_id="run-a", step_id=0, tool_calls=(bad_tc,)), [proc])
        assert proc._error_counts.get("run-a") == 1

        # Run B gets a valid response — must NOT touch run A's counter
        await pipe(ModelResponseEvent(run_id="run-b", step_id=0, tool_calls=(good_tc,)), [proc])
        assert proc._error_counts.get("run-b") is None  # cleaned up on valid
        assert proc._error_counts.get("run-a") == 1  # run A unchanged

        # Run A gets a second malformed response — now raises
        with pytest.raises(ModelParseError):
            await pipe(ModelResponseEvent(run_id="run-a", step_id=1, tool_calls=(bad_tc,)), [proc])
        assert proc._error_counts.get("run-a") is None  # cleaned up after raise

