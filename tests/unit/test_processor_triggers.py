def _triggers(events: list) -> list[ProcessorTriggerEvent]:
    """Filter only ProcessorTriggerEvent entries from a pipe_all result."""
    return [e for e in events if isinstance(e, ProcessorTriggerEvent)]


class _RecordingTracer:
    """Minimal tracer that records ProcessorTriggerEvent auto-generated by ProcessorChain."""

    def __init__(self) -> None:
        self.trigger_events: list[ProcessorTriggerEvent] = []
        self.raw_events: list = []

    async def on_event(self, event) -> None:
        if isinstance(event, ProcessorTriggerEvent):
            self.trigger_events.append(event)

    async def on_raw_event(self, event) -> None:
        self.raw_events.append(event)


